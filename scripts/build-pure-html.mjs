/**
 * 由 data/events.json 產生純靜態 immba-pure.html
 * （不依賴 React、Vite、fetch；可 file:// 或任意靜態主機直接開啟）
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

function esc(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function escAttr(s) {
  return esc(s).replace(/'/g, "&#39;");
}

function eventDateMs(s) {
  const t = Date.parse(String(s).trim());
  return Number.isFinite(t) ? t : 0;
}

function sortEvents(list) {
  return [...list].sort((a, b) => eventDateMs(b.date) - eventDateMs(a.date));
}

function normalizeEvent(raw) {
  if (!raw || typeof raw !== "object") return null;
  const title = String(raw.title ?? "").trim();
  const date = String(raw.date ?? "").trim();
  const body = String(raw.body ?? raw.content ?? "");
  const image = raw.image ?? raw.imageUrl;
  const imageUrl =
    image != null && String(image).trim() ? String(image).trim() : "";
  if (!title || !date) return null;
  const hashtags = Array.isArray(raw.hashtags)
    ? raw.hashtags.map((x) => String(x).trim()).filter(Boolean)
    : [];
  const titleEn = String(raw.titleEn ?? "").trim();
  const bodyEn = String(raw.bodyEn ?? raw.contentEn ?? "").trim();
  const dateZh = String(raw.dateZh ?? "").trim();
  return { title, date, body, imageUrl, hashtags, titleEn, bodyEn, dateZh };
}

const evPath = path.join(ROOT, "data", "events.json");
const outPath = path.join(ROOT, "immba-pure.html");

const evFileUtf8 = fs.readFileSync(evPath, "utf8");
const evJson = JSON.parse(evFileUtf8);
const eventsEmbedJson = evFileUtf8.trim();
const events = sortEvents(
  (evJson.events || []).map(normalizeEvent).filter(Boolean)
);

const GALLERY_URL =
  "https://www.management.fju.edu.tw/subweb/immba/subedit.php?EID=241";

const eventCards = events
  .map((ev) => {
    const imgBlock = ev.imageUrl
      ? `      <div class="pure-event-card__media">
        <img class="pure-event-card__img" src="${escAttr(ev.imageUrl)}" alt="" width="1200" height="750" loading="lazy" decoding="async" />
      </div>`
      : "";
    const tagBlock =
      ev.hashtags && ev.hashtags.length
        ? `\n        <p class="pure-event-card__hashtags">${ev.hashtags.map((t) => esc(String(t))).join(" ")}</p>`
        : "";
    const titleEnBlock = ev.titleEn
      ? `\n        <p class="pure-event-card__title-en" lang="en">${esc(ev.titleEn)}</p>`
      : "";
    const bodyEnBlock = ev.bodyEn
      ? `\n        <div class="pure-event-card__content-en" lang="en">${esc(ev.bodyEn)}</div>`
      : "";
    const dateLine = esc(ev.dateZh || ev.date);
    return `      <article class="pure-event-card">
${imgBlock}
      <div class="pure-event-card__body">
        <h3 class="pure-event-card__title">${esc(ev.title)}</h3>${titleEnBlock}
        <div class="pure-event-card__date">${dateLine}</div>
        <div class="pure-event-card__content">${esc(ev.body)}</div>${bodyEnBlock}${tagBlock}
      </div>
    </article>`;
  })
  .join("\n");

const tplPath = path.join(ROOT, "scripts", "pure-template.html");
let html = fs.readFileSync(tplPath, "utf8");
html = html.replace(
  "<!--INJECT_META_COMMENT-->",
  "<!-- Generated by scripts/build-pure-html.mjs or npm run build:pure -->"
);
html = html.replace("<!--INJECT_EVENTS-->", eventCards);
html = html.replace("<!--INJECT_EVENTS_EMBED_JSON-->", eventsEmbedJson);
html = html.replace("GALLERY_URL_PLACEHOLDER", escAttr(GALLERY_URL));

fs.writeFileSync(outPath, html, "utf8");
console.log("Wrote", path.relative(ROOT, outPath));
